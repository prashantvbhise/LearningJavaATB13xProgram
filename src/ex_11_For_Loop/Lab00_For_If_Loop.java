package ex_11_For_Loop;

public class Lab00_For_If_Loop {
    public static void main(String[] args) {
        // for ( I ; C ; U )
        for (int shruti = 0; shruti < 18; shruti++) {
            if (shruti > 15) {
                System.out.println("Shurti is Grownup");
            } else {
                System.out.println("Shurti is teenager");
            }
        }

    }
}
