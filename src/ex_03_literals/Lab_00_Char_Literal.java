package ex_03_literals;

public class Lab_00_Char_Literal {
    public static void main(String[] args) {

        char new_line='\n';
        char tab_line='\t';
        char back_space='\b';
        char carriage_return='\r';

        System.out.println("PrashantBhise");
        System.out.println("Prashant" +new_line+ "Bhise");
        System.out.println("Prashant" +tab_line+ "Bhise");
        System.out.println("Prashant" +back_space+ "Bhise");
        System.out.println("Prashant" +carriage_return+ "Bhise");
        System.out.println("Hi, This is a First line"+new_line+"This is second line\n This is Third line");
    }
}
